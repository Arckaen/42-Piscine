int		ft_sqrt(int nb)
{
	int i;

	i = 2;
	if (nb == 1)
		return (1);
	while (i <= nb / i)
	{
		if (nb % i == 0 && nb / i == i)
			return (i);
		i++;
	}
	return (0);
}
