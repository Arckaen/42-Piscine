char    *ft_strcapitalize(char *str)
{
	int i;
				    
	i = 0;
	while(str[i] != '\0')
	{
        if (i == 0 && str[i] > 96 && str[i] < 123)
            str[i] = str[i] - 32;
		i++;
        if ((str[i-1] > 31 && str[i-1] < 48) || (str[i-1] > 122 && str[i-1] < 127))
		{
            if(str[i] > 96 && str[i] < 123)
            {
                str[i] = str[i] - 32;
            }
        }
		else if (str[i] > 64 && str[i] < 90)
		{
            if (str[i-1] < 123 && str[i-1] > 96 || str[i-1] > 64 && str[i-1] < 91)
                str[i] = str[i] + 32;
		}
	}
    return (str);
}
