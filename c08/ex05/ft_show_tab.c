#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else if (nb >= 0 && nb <= 9)
	{
		ft_putchar(nb + '0');
	}
	else if (nb < 0)
	{
		ft_putchar('-');
		ft_putnbr(nb * (-1));
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	ft_putchar('\n');
}

void	ft_show_tab(struct s_stock_str *strs)
{
	int i;

	i = 0;
	while (strs[i].str)
	{
		ft_putstr(strs[i].str);
		ft_putnbr(strs[i].size);
		ft_putchar('\n');
		ft_putstr(strs[i].copy);
		i++;
	}
}
