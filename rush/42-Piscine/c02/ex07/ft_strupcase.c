int        *ft_strupcase(char *str);
{
	int i;
		    
	i = 0;
	while(str[i] != '\0')
	{
		i++;
		if (str[i] < 123 && str[i] > 96)
		{
			str[i] = str[i] + 32;
		}
	}
}
